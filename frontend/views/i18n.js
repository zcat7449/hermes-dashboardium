  let currentLang = localStorage.getItem(LANG_KEY) || DEFAULT_LANG;
  if (!translations[currentLang]) currentLang = DEFAULT_LANG;

  function t(key) {
    const lang = translations[currentLang];
    if (lang && lang[key] !== undefined) return lang[key];
    // fallback to ru
    const fallback = translations.ru;
    return fallback && fallback[key] !== undefined ? fallback[key] : key;
  }

  function setLang(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    localStorage.setItem(LANG_KEY, lang);
    // Re-render everything
    if (window.Dashboard && window.Dashboard.Render) {
      window.Dashboard.Render.renderAll();
    }
  }

  function getLang() {
    return currentLang;
  }

  function getLangs() {
    return Object.keys(translations);
  }

  window.Dashboard.I18n = { t, setLang, getLang, getLangs };
